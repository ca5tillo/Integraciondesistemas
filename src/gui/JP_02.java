    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // Enviar SOAP al server
        String ip = server.split(":")[0];
        int puerto =  Integer.parseInt(server.split(":")[1]);
        String msg = jTextArea_Request.getText();
        String SOAP_RESPONSE;
        String ssss ;
        
        //msg = msg.replaceAll("\n", " "); //Quitar saltos de linea
        String ss = Main.getIntegracion().get_Lenguaje_del_servidor();
        
        
        if( ss != null && (ss.equals("c") || ss.equals("C"))){
            SOAP_RESPONSE = new Cliente(ip, puerto, msg, "c").get();
           
        }else{
            SOAP_RESPONSE = new Cliente(ip, puerto, msg, "j").get();
        }
        
        if(SOAP_RESPONSE != null){
            jTextArea_Response.setText(Main.getIntegracion().get_XML_Response(SOAP_RESPONSE));
            ssss = Main.getIntegracion().get_resultado_response();
        
        }else{
            ssss = "Error al conectarce al servidor";
        }
        
        jTextArea_resultadoPantalla.setText(ssss);
        
    }//GEN-LAST:event_jButton3ActionPerformed
