    private void jTextArea_RequestKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextArea_RequestKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_CONTROL || 157 == evt.getKeyCode()) {
            swcontrol = true;
        } else if (swcontrol && evt.getKeyCode() == KeyEvent.VK_A) {
            jTextArea_Request.selectAll();
            swcontrol = false;
        }else if (swcontrol && evt.getKeyCode() == KeyEvent.VK_C) {
            jTextArea_Request.copy();
            swcontrol = false;
        }else if (swcontrol && evt.getKeyCode() == KeyEvent.VK_V) {
            jTextArea_Request.paste();
            swcontrol = false;
        }
    }//GEN-LAST:event_jTextArea_RequestKeyPressed

